<div id="driver" class="tab-pane fade">
<p>Most of the features which account for the variability of temperature and rainfall in Trinidad and Tobago originate in the tropics, but global features also impact the climatic variability. Phenomena such as El Ni&ntilde;o-Southern Oscillation (ENSO) and the North Atlantic Oscillation (NAO) are two of the major drivers of climate variability in Trinidad and Tobago. It has also been shown that the Madden-Julian Oscillation (MJO), the Saharan Air layer (SAL), tracks taken by tropical storms, migratory behaviour of the Inter-tropical Convergence Zone (ITCZ) and behaviour of the North Atlantic Sub-Tropical High (NASH) also influence the climate variability in Trinidad and Tobago.</p>
<p>Studies have shown that the El Nino phase of the ENSO modifies the annual cycle of rainfall over the Caribbean greatest, with varying results for different areas. The relationship between El Nino and the rainfall cycle starts with a drying trend during the latter half of the wet season which continues into the dry season and ends with a strong wet signal during the earliest period of the new wet season; this relationship was observed in Trinidad and Tobago during the 2009-2010 episode of the most recent El Nino event. With regards to La Nina, it has been found that the Southern Caribbean including Trinidad and Tobago exhibits wetter than normal late wet season and dry season; while the early wet season at the end of a La Nina event is drier than normal. The link between the NAO and Caribbean rainfall is found in the relationship between pressure systems and rainfall and temperatures in the region. A positive index is indicative of anomalously high pressures across the region resulting in a reduction in rainfall since higher pressure introduces stronger winds, more evaporation, cooler Sea Surface Temperatures (SST's), and less ascending motion. When the index is negative, rainfall in the early period of the wet season is enhanced since lower anomalous pressure introduces lighter winds, less evaporation, warmer SST's, and more ascending motion.</p>
</div>
<div id="trends" class="tab-pane fade">
<p>Several studies exist which have found recent changes in the climate in Trinidad and Tobago, especially with respect to temperature trends. Temperatures in Trinidad and Tobago have been found to have increased steadily from 1946 to 1995. More recently, it has been shown that over the last three decades there has been an upward trend in temperatures that is statistically significant and which has been induced by a steady increase in daily minimum temperatures ; this latter finding suggests that nights have become warmer. Other studies are also consistent with the warmer temperatures. Mc Sweeney 3using a Global Climate Model (GCM) found significant increases in the mean annual temperature in Trinidad and Tobago with an increase around 0.6 <sup>o</sup>C since 1960; an average of 0.13 <sup>o</sup>C per decade. The Trinidad and Tobago Meteorological Service (TTMS) also found that at Trinidad's, as well as, at Tobago's reference climatological stations, the annual mean air temperature has also warmed over the period 1981-2010 by 0.8 and 0.5 <sup>o</sup>C relative to 1961-1990 and 1971-1990, for Trinidad and Tobago respectively. The observed anomalous warming of 0.27 C per decade at the station in Trinidad and 0.17 <sup>o</sup>C per decade in Tobago are consistent with the IPCC (2007) observed 0.2 <sup>o</sup>C per decade in the Caribbean region.</p>
<p>Rainfall in Trinidad and Tobago has not shown as distinct a change as has been observed with temperatures. It appears that changes in rainfall differ across various parts of the country and over different time periods. During the 1960's to 80's it has been shown that Trinidad and Tobago annual rainfall totals increased, but subsequently decreased onward to the 1990's. More recently, rainfall in Penal in South Trinidad has showed a decreasing trend while at Piarco in the north, an increasing trend was observed. Another recent study conducted on behalf of the UNDP found fractional decreases in Trinidad and Tobago's rainfall over the last half century; however the trend was not statistically significant. The TTMS 8 also found that there has not been any significant change in the annual mean rainfall totals in Trinidad over the period 1981 to 2010 compared to 1961-1990; however, there were larger changes in the annual mean rainfall totals over the latter period. These changes were driven by slight increases in the frequency of extreme dry and wet years, as well as, increases and decreases in extreme wet and dry years rainfall totals respectively. In Tobago however, the rainfall pattern has shown a different behaviour;Tobago's annual mean rainfall has decline by 36.4 mm per decade during the 1981-2010 period compared to the long term average of the 1969 to 1990 period, this was driven primarily by a decrease in the number of years with near normal rainfall.</p>
<p>Strong El Nino events in recent years have resulted in meteorological droughts in Trinidad and Tobago. It has also been shown that the El Nino of 1997/98 and associated drought conditions in Trinidad and Tobago were responsible for the decline in sugar production during that period. The Drought event of 2010 in Trinidad and Tobago resulted in hundreds of acres of natural forest burnt. Drought conditions have also been advanced as the reason for lost of near- shore habitat due to declines in freshwater. This was aided by saline intrusion in the Southern Caribbean and Trinidad and Tobago during 2009-2010. Increased sea surface temperatures during the summer of 2005 resulted in severe coral bleaching in Tobago during the summer period. Recent increases in intense rainfall events have been associated with increased flooding in the region including Trinidad and Tobago and this has no doubt impacted fresh water impoundments and water quality. The Caribbean region experienced an average sea level rise of about 10 cm over the twentieth century due to the warming; but this rise is not uniform throughout the region.</p>
</div>
<div id="projections" class="tab-pane fade">
<p>Statistical downscaling of global climate model (GCM) HadCM3 using the A2 and B2 emission scenarios project a rise in temperature of between 1.5 - 2.0 Celsius in the Caribbean region including Trinidad and Tobago by the 2080's compared to the 1980 to 1999 period. This amounts to a projection of 0.29 Celsius per decade. If these projections are realized it is possible that an approximate 8% increase in the length of the dry season by 2050 and consequently a shorter wet season could result. Projected trends in rainfall for Trinidad and Tobago remain unclear with large uncertainties. Model consensus among 21 GCM models project annual rainfall decrease in the range of 15% for Trinidad and Tobago by the 2080's, however 3-4 of these models projected increase in annual rainfall. If the over drying trend projections are realized then it is likely that there will be intensification of dry spells and droughts in the vicinity of Trinidad and Tobago towards the 2080's.</p>
<p>Results from HadCM3 model downscaled for the A2 and B2 emission scenarios using the Statistical Downscale Modeling (SDSM) also project increases in the early rainy season (June &ndash; August) rainfall in Trinidad and Tobago compared to decreases in other countries in the region apart from Barbados. The drying trend projected in annual rainfall towards the 2080's is linked to a consensus in the models showing a shift to a more positive phase of the North Atlantic Oscillation (NAO) and El Nino like pattern in the Pacific with higher temperatures.</p>
<p>Based on the projections local water resources can be expected to be impacted. Increase intense rainfall events over shorter periods will result in lower surface water quality, reduction in the recharge of ground water as run off would be at a maximum; while Increase in longer dry spells and drought events coupled with warmer temperatures would increase agricultural irrigation demands,affect crop scheduling, increase heat related health impacts, coral bleaching and saline intrusion.</p>
<p>Recent modelling of the current and future water resource availability on several small islands in the Caribbean including Trinidad and Tobago, using a macro-scale hydrological model and the SRES scenarios (Arnell, 2004), found that many of these islands would be exposed to severe water stress under all SRES scenarios, and especially so under A2 and B25. Also, recent variations in sea level on the western Trinidad coast indicate that sea level in the north is rising at a rate of about 1 mm/yr, while in the south the rate is about 4 mm/yr; the difference being a response to tectonic movements 6. According to IPCC AR4 global Sea level is projected to rise between the present (1980-1999) and the end of this century (2090-2099) by 0.35 m(0.23 to 0.47 m) for the A1B scenario. Based on research evidence published over most recently, mean global sea level rise is projected to increase above that of IPCC AR4 projections by the end of the century7 8 9 10. Furthermore, recent studies also suggest that because of the close proximity of the Caribbean including Trinidad and Tobago to the equator, sea level rise may be more outstanding than some other regions.11 12 This suggests that, in the future, if projections materialize, coastal inundation, inland flooding, storm surge damage, and coastal erosion are likely to increase in the vicinity of Trinidad and Tobago, however uncertainty remains high.</p>
