<li>
<p>The Standard Precipitation Index (SPI) recommended by the World Meteorological Organization for monitoring dry spells and characterizing droughts by National Meteorological and Hydrological Services be adopted as the index which the Trinidad and Tobago Meteorological Service will utilize in monitoring and attempting to predict local dry spells and meteorological droughts.</p>
<p>The SPI indicates how rainfall for any given duration (1-month, 2- month, 3- month, etc) at a particular observing station compares with the long-term rainfall record at the same station for the same duration. Conceptually, the SPI to be used is equivalent to the z-score used in statistics: Z-score= (X - Average) / Standard Deviation</p>
<p>Because rainfall is typically skewed, the rainfall data should first be transformed to fit a normal distribution by applying a gamma function. After the transformation, the SPI is to be calculated using:</p>
<p>SPI = (A - A) / Standard Deviation,</p>
<p>Where A is the total rainfall for the duration being currently considered; A is the average rainfall for the same duration over the historical records; and the SPI expresses A's distance from the average (A) in standard deviation units.</p>
<p>Since the SPI values are to be obtained from the standard normal distribution, the unit of the SPI should be interpreted as standard deviations.</p>
