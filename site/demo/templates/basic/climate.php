<p class="indent">Trinidad and Tobago's close proximity to the equator enables the country to have two climate types producing two opposing seasons. These seasons are differentiated by distinct dry and wet season regimes. The dry season which occurs during January to May is symbolized by a tropical maritime climate that is characterized by moderate to strong low level winds, warm days and cool nights, with rainfall mostly in the form of showers due to daytime convection. A modified moist equatorial climate characterized by low wind speeds, hot humid days and nights, a marked increase in rainfall which results mostly from migrating and latitudinal shifting equatorial weather systems, symbolizes the wet season during June to December. The periods late May and December are considered as transitional periods to the wet and dry seasons respectively. Variations in these two climatic seasons between the islands of Trinidad and Tobago are primarily as a result of difference in land size, orography, elevation, orientation in terms of the trade winds and geographical location. Within the wet season is the hurricane season which runs from June to November, peaking between August and October. Trinidad&rsquo;s geographical location puts it on the southern periphery of the North Atlantic hurricane basin. As such, Trinidad is not affected directly by storms as frequent as Tobago; however, peripheral weather associated with the passage of tropical storm systems impacts Trinidad and Tobago similarly.</p>
<p>Trinidad and Tobago's daily temperature cycle is more pronounced than its seasonal cycle. The long term mean (1971 - 2000) annual maximum and minimum temperatures are 31.3 Celsius and 22.7 Celsius respectively with a mean daily temperature of 26.5 Celsius. Generally, the wet season temperatures are warmer than the dry season temperatures with September being the warmest wet season month and March the warmest in the dry season. Annually and seasonally Trinidad is wetter than Tobago; however, the rainfall pattern in both islands display a distinct bi-modal behaviour with early (June) and late(November) rainfall season maxima occurring. Trinidad's primary rainfall mode occurs in June while Tobago's primary mode occurs in November. The annual rainfall totals are largely driven by multiple competing weather features, chief of which are the latitudinal position and strength of the North Atlantic Sub-Tropical High (NASH) pressure cell, meridional shift of the Inter-Tropical Convergence Zone (ITCZ), westward propagating Tropical Waves and cyclones (depression, storms, hurricanes), Mid-Atlantic upper level trough system, localized sea-breeze effects, cloud clusters driven by large scale low level convergence and orography. Generally, during the dry season the NASH pressure cell center migrates more southerly than during the wet season and expands equator-ward resulting in generally subsiding air, low level moisture evacuation and strong trade winds over Trinidad and Tobago, which result in a drier state of the atmosphere. Usually during May, the slow but sure shifting of the NASH pole-ward with winds on its southern flank converging more, allows rain bearing systems such as the ITCZ and Tropical waves to penetrate northward and eastward; resulting in the transition to and eventual onset of the wet season regime.</p>
</div>
<div id="vc" class="tab-pane fade">
<p><strong>Climate Variability</strong> refers to departures from the mean position and higher order statistics such as standard deviations, as well as changes in the occurrences of extremes. Moreover, it is used to indicate departures of the mean etc over a given time period (for instance a month, a season, a year) from the long term statistic for a matching time period. Climate variability is thus a measure of the departures which are usually called anomalies. Climate variability is caused by natural occurring internal processes which occur on all time and spatial scales outside of that of individual weather events, and involves many modes of variability involving components of the climate systems such as the atmosphere and the ocean. An example of a naturally occurring internal process which drives climate variability in Trinidad and Tobago is the coupled ocean/atmosphere El Nino-Southern Oscillation (ENSO- El Nino or La Nina) phenomenon.</p>
<p><strong>Climate change</strong> refers to a change in the mean condition of the climate that can be identified by a change in the mean and or variability of its properties that is statistically significant and this change must continue for an extended period, typically decades or longer. It may be due to naturally occurring internal processes such as volcanic eruptions which can act to cool the atmosphere; or external forcing such as changes in solar radiation received at the earth surface which can be influenced for instance by change in the tilt of the earth axis away from its orbital plane which occurs naturally. Naturally occurring climate variability can also induce climate change. Climate change may also be due to persistent external influences which do not occur naturally such as the change in the composition of the atmosphere or change in land use generated by human activity such as the burning of fossil fuel and deforestation.</p>
<p>The major difference between climate variability and climate change is the persistent nature of the anomaly and the fact that the change cannot be explained by naturally occurring internal variability processes alone. For instance, rare events occur more often or less often e.g. the maximum temperatures increasingly breaking records each year or nights are consistently becoming warmer each year. Climate change detection is the process of demonstrating that the climate has changed in some defined statistical sense without providing a reason for the change. Climate change attribution is the process of establishing the most likely cause for the detected change with some defined level of confidence.</p>
</div>
<div id="DP" class="tab-pane fade">
<p><strong>1. Statement of the problem</strong></p>
<p>The intent of this document is to facilitate monitoring and assessment of mild to extreme dry spells in Trinidad and Tobago; and in the process assist in reducing crisis responses to such events. Motivation for this policy document resulted from the reality that varying degrees of dry spells in Trinidad and Tobago impact water resources management, agricultural production, health and well-being as well as other areas. More importantly, extreme dry spells such as drought conditions can have related ripple effect on the economy, in areas such as agriculture and related sectors, including forestry, especially in the circumstances of projected climate change. In the context of an increasing local population, a renewed push towards agriculture productivity, the associated increase in demand for potable water, and the underlying vulnerability from extreme dry spell impacts, it is imperative that as a national meteorological service, proper monitoring of dry spells with regular updates be established. This would require a dry spell or drought early warning system that incorporates monitoring, assessment and a well developed information delivery system.</p>
<p>Current operative procedures for determining different levels of dry spells are not adequately defined. Consequently, the meteorological service intention of moving towards science based dry spell and drought monitoring is somewhat stymied. This policy document seeks to remove the gap and is expected to serve as a basic policy guidance document on essential rainfall deficit or surplus monitoring aimed at ensuring that the Trinidad and Tobago Meteorological Service (TTMS) offers quality advice to key stakeholders and decision makers. Basic international and regional standards and guidelines for monitoring and assessing rainfall deficits and surpluses have long been in place under the auspices of the World Meteorological Organisation (WMO) and have undergone upgrade and repositioning from time to time. This document seeks to similarly follow this practice at the local level.</p>
<p><strong>2. Introduction</strong></p>
<p>Before implementing any dry spell and drought policy it is important to understand the difference between the two features, the complexity of the latter and distinguish between the various types of drought. According to WMO a dry spell is a period of at least 15 consecutive days none of which received 1 mm or more (UK) or a period lasting not less than two weeks during which no measurable rainfall is recorded (US). Generally the term is applied to less extensive and less severe conditions than drought. Drought on the other hand is broadly defined by the WMO as an extreme climatic condition that results from an extended period of decrease rainfall that is significantly less than the expected amount for a specific period and is not enough to meet the demands of society activities as well as the environment. In essence, drought is an extended, unusually dry period when there is not enough water to meet the normal needs of users and is not just about low rainfall.</p>
<p>Drought is very application specific and has different meanings based on water needs of various sectors. For instance, climatologists keep a close watch on the extent and severity of drought in terms of rainfall deficit; agriculturalists consider the impact of water shortage on crop growth; hydrologists evaluate ground water levels; and sociologists classify it on social expectations and beliefs. Accordingly, various types of droughts exist such as meteorological, agricultural, hydrological and socio-economic. This policy document deals specifically with meteorological drought and associated dry spells. In guiding National Meteorological Services (NWS), WMO suggests that the Standard Precipitation Index (SPI) become the primary index to monitor dry spells and characterize meteorological drought. The TTMS adopts the SPI as its primary dry spell and drought monitoring index and will supplement its dry spell, drought monitoring and assessment process from time to time with any other indices which it may deem fitting for effective dry spell and drought assessment. To highlight what is broadly considered a meteorological drought in the context of other droughts the following definitions are offered.</p>
<p><strong>3. Definitions</strong></p>
<p>Meteorological drought refers to short-period droughts or dry spells and is based solely on deficiency in rainfall that is far below the expected average (usually the period long term average) over a specific extended period of time, usually a few months, a season, to a few years. It is expressed solely on the basis of the degree of dryness (often in comparison to some "normal" or average amount) and the duration of the dry period. Since atmospheric conditions which results in rainfall deficiencies vary according to regions, meteorological drought is region specific and as such its characterization must take into consideration a profound understanding of the climatology of the particular area. It may consider: the number of days with rainfall below some specified threshold or the actual rainfall departures from average totals on monthly, seasonal or yearly time scales.</p>
<p>AGRICULTURAL DROUGHT</p>
<p>Agricultural drought is defined by the measure of the availability of soil water to plants or animals and occurs when there is not enough rainfall, soil moisture and difference between actual and potential evapo-transpiration to meet the needs of a specific plant or animal at a particular time. It can take place simultaneously or a little before or after a meteorological drought.</p>
<p>HYDROLOGICAL DROUGHT</p>
<p>Hydrological drought refers to deficits in surface and sub-surface water supplies based on measurements of stream flow, lake, reservoir and groundwater levels when rainfall is deficient during an extended period of time. Hydrological droughts are usually out of phase or lag the occurrence of meteorological and agricultural droughts.</p>
<p>SOCIO-ECONOMIC DROUGHT</p>
<p>Socio-economic drought definitions are associated with the effect of supply and demand of a product with elements of meteorological, agricultural and hydrological drought. Socio-economic drought occurs when demand for an economic good exceeds supply as a result of physical water shortage that is weather related and starts to affect people, both individually and collectively.</p>
<p><strong>4. TTMS Meteorological Drought and Dry Spell Characterization Methodology</strong></p>
<p>Defining a local meteorological drought on the basis of number of days with rainfall below some specified threshold is unrealistic for Trinidad and Tobago given that extended (14 to 20 consecutive day- a dry spell) number of days without rainfall is a common feature of the local dry season climatology. It is also not unusual to experience extended number of dry days (Petit Careme) in the wet season. A more realistic method is actual rainfall departures from average totals on monthly or seasonal time scales given that it is not normal to have a month much less a season without measurable rainfall in the local dry season, climatology.</p>
<p>Operationally, the TTMS meteorological drought and dry spell characterization seeks to identify the onset, duration and severity of the event through the comparison of the existing situation with the climatological average based on the 30 year climatological period or longer in accordance with the WMO recommendations.</p>
<p>In utilizing the SPI as the primary dry spell and drought monitoring tool, the TTMS take note of, and adhere to the following principles about the SPI from WMO (2012), McKee (1993) and Guttman (1999) : The SPI provides a comparison of the rainfall over a specific period (e.g. a specific 1-, 2-, 3-, 6-, 12-month period) with the rainfall totals for the corresponding period for all the years in the climatological record.</p>
<p>The SPI is a drought index based only on rainfall and represents the number of standard deviations that observed cumulative rainfall move away from the climatological or long term average for normally distributed rainfall. Since rainfall is not normally distributed, in calculating the SPI, an appropriate probability density function is estimated and a transformation is applied to the associated cumulative probability distribution so that the rainfall values follow a normal distribution.</p>
<p>The resulting SPI is then interpreted as a probability using the standard normal distribution (i.e., users can expect the SPI to be within one standard deviation about 68% of the time, two standard deviations about 95% of the time, etc.). Therefore, the SPI for a time scale is the probability of recording a given amount of rainfall and since the probabilities are standardized, zero SPI indicates the mean rainfall amount is expected. Negative SPI indicates expected deficit rainfall (i.e. number of standard deviations that the rainfall value would be less than the mean during the time scale considered);while positive SPI indicates surplus rainfall (i.e. number of standard deviations that the observed rainfall value would be higher than the mean during the time scale considered.</p>
<p>Meteorological conditions respond to rainfall anomalies on relatively short time scales. Climatologically, it has been observed locally that high temperatures, relative humidity, and wind and soil moisture, influence and is influenced by rainfall totals. This interplay is critical in assessing and monitoring the duration and severity of rainfall deficits and is given utmost consideration in characterizing what conditions need to exist locally to consider the occurrence of a Meteorological Drought in Trinidad and Tobago. The characteristics of past dry spells and the impacts caused on water catchment areas also provide a benchmark in defining the local meteorological drought and what similar conditions in the future may warrant such a declaration.</p>
<p>The onset, duration and severity of a local dry spell and a meteorological drought is characterized using a 2-month SPI which compares a 2-month rainfall total for a given station with rainfall total from the same 2-month for all the years on record for the same station. For instance a 2-month SPI at the end of December 2012 for the Piarco rainfall station compares the November and December rainfall totals in that year with November-December rainfall totals for all years on record for the station.</p>
<p>A local dry spell is considered to occur when the 2-month SPI falls between -1.0 and -1.24; a 2-month SPI between -1.25 to -1.49 is considered a moderate dry spell; a 2-month SPI between -1.50 to -1.99 over two consecutive 2-month timescale is considered a drought event; a 2-month SPI of -2.0 and less over two consecutive 2-month time scale is considered a severe drought. A 2-month SPI between -1.50 to -1.99 for one 2-month time scale is considered an incipient (budding) drought event and a drought watch or alert may be issued. Similarly, a 2-month SPI of -2.0 and less for one 2-month timescale is considered an incipient (budding) severe drought event.</p>
<p><strong>5. Conclusion</strong></p>
<p>Traditionally, local response to dry spells of all degrees has been through a reactionary and at times, a crisis management approach. This policy responds to the need to develop an appropriate and adequate early warning system for dry spells of all degrees. The present policy would assist in closing the gap in efficient science based monitoring of local dry spells and meteorological drought and improve assessment of these events. It would also establish a framework to advance both short and medium-term responses across all sectors to all local dry spells. A comprehensive dry spell and meteorological drought monitoring and early warning system needs a guidance policy which can deliver onset and end, determine their severity, and convey this information to key stakeholders in climate- and water-sensitive sectors in a timely manner. This policy would facilitate the delivery of such information.</p>
<strong>6. Operational Policy</strong>
<p>In response to the need for a practicable dry spell and meteorological drought monitoring and forecast application, the Acting Director, Trinidad and Tobago Meteorological Service, recommends the following operational policy:</p>
